#include "libftprintf.h"

void	ft_putendl(char const *s)
{
	int r;

	r = 0;
	if (!s)
		return ;
	while (s[r])
	{
		write(1, &s[r], 1);
		r++;
	}
	write(1, "\n", 1);
}
